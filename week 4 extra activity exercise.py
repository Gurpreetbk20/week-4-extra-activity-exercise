# Assigning multiple variables in a single statement
a, b, c = 1, 2, 3

print("a =", a)
print("b =", b)
print("c =", c)

# Tuple unpacking
x, y, z = (4, 5, 6)

print("x =", x)
print("y =", y)
print("z =", z)

# Swapping variables
x = 10
y = 20
x, y = y, x

print("x =", x)
print("y =", y)
